    public void list_users()
    {
        try
        {
            con = dbConnect.connect();
            
            String query = "select * from user";
            state = con.createStatement();
            rs = state.executeQuery(query);
            
            while(rs.next())
            {
                String midname = rs.getString("Mid_name");
                
                ass_user.addItem(rs.getString("First_name") +" "+midname.charAt(0)+". "+ rs.getString("Last_Name"));
            }
        }catch(Exception e)
        {
            
        }
    }
    public void list_course()
    {
        try
        {
            con = dbConnect.connect();
            
            String query = "select * from course";
            state = con.createStatement();
            rs = state.executeQuery(query);
            
            while(rs.next())
            {
                course.addItem(rs.getString("course_name"));
            }
        }
        catch(Exception e)
        {
            
        }
    }

