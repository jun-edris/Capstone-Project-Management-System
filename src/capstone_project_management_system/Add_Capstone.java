    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int select = ass_user.getSelectedIndex();
        try
        {
            con = dbConnect.connect();
            
            String query = "insert into capstone values(NULL, ?,'"+select+"','"+course.getSelectedIndex()+"')";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, school1.getText() +" - "+ school.getText());
            ps.execute();
            
            Show_user Show_user = new Show_user();
            Show_user.setVisible(true);
            this.dispose();
            JOptionPane.showMessageDialog(null, "success");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

